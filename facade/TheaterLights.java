public class TheaterLights {

	public TheaterLights() {}

	public void on() {
		System.out.println("The TheaterLights are now on.");
	}

	public void off() {
		System.out.println("The TheaterLights are now off.");
	}

	public void dim(int brightness) {
		System.out.println("The TheaterLights are now dimmed to a brightness of " + brightness + ".");
	}

	// public String toString() {}

}
